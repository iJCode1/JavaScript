// Esctructura Switch en JS

var n = 3;

switch(n){
    case 1:
        console.log("Numero 1!");
        break;
    case 2:
        console.log("Numero 2!");
        break;
    case 3:
        console.log("Numero 3");
        break;
    default:
        console.log("Caso no encontrado!");
        break;
}